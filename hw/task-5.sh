#!/bin/bash

set -m

echo "Запускаем фоновые задачи..."
sleep 10 &
echo "Задача 1 (sleep 10) запущена в фоновом режиме."

sleep 11 &
echo "Задача 2 (sleep 11) запущена в фоновом режиме."

sleep 9 &
echo "Задача 3 (sleep 9) запущена в фоновом режиме."

echo "Текущие фоновые задачи:"
jobs

echo "Приостанавливаем первую задачу..."
kill -STOP %1
echo "Первая задача приостановлена."

echo "Состояние фоновых задач после приостановки:"
jobs

echo "Возобновляем первую задачу в фоновом режиме..."
bg %1

echo "Состояние фоновых задач после возобновления:"
jobs

wait

echo "Все задачи завершены."
